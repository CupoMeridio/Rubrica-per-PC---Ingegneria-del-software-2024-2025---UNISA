/**
 * @package it.unisa.diem.team02.contactsbook.ui.controllers
 * 
 * Descrizione del package:
 * Questo package contiene i controller dell'interfaccia utente per la rubrica dei contatti (ContactsBook). 
 * I controller gestiscono le interazioni tra l'utente e l'applicazione, consentendo di visualizzare, 
 * aggiungere, modificare e rimuovere contatti dalla rubrica.
 * 
 * Le classi in questo package implementano la logica di controllo per le varie schermate dell'applicazione, 
 * come la visualizzazione del catalogo dei contatti, l'aggiunta di nuovi contatti e la modifica delle informazioni 
 * esistenti. I controller seguono il pattern di progettazione MVC (Model-View-Controller) per separare la logica di 
 * business dalla presentazione e dall'interfaccia utente.
 * 
 * Classi principali:
 * - AddViewController: Gestisce le operazioni relative all'aggiunta dei contatti alla rubrica
 * - ContactsbookViewController: Gestisce la visualizzazione e la gestione della lista di contatti, nonchè i tasti per accedere alle singole funzioni della rubrica
 * - ModifyContactsbookViewController: Gestisce le operazioni relative alla modifica dei contatti già presenti in rubrica
 * - DuplicateContactViewController: Gestisce le operazioni da eseguire nei casi in cui dovessero essere presenti contatti duplicati
 * - ErrorMessaViewController: Gestisce l'interfaccia dedita alla visualizzazione degli errori da far visualizzare a schermo
 * - LoginViewController: Gestisce le operazioni relative ale operazioni di login e di registrazione dell'utente.
 */
package it.unisa.diem.team02.contactsbook.ui.controllers;
