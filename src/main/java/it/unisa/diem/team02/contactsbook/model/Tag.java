
package it.unisa.diem.team02.contactsbook.model;

/**
 * @brief Enum che rappresenta le possibili etichette per un contatto.
 * 
 * Questa enum definisce una serie di etichette predefinite che possono essere associate a un contatto.
 * Le etichette includono categorie comuni come "Home", "University" e "Job", che rappresentano 
 * i contatti associati a casa, università e lavoro rispettivamente.
 * 
 * @details 
 * - `Home`: rappresenta un contatto associato alla casa.
 * - `University`: rappresenta un contatto associato all'università.
 * - `Job`: rappresenta un contatto associato al lavoro.
 * 
 * @note L'enum può essere esteso in futuro per includere altre etichette, a seconda delle necessità dell'applicazione.
 */
public enum Tag{
    Home,
    University,
    Job
}
